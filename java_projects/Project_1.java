public class Project_1 {
	// The Function
	public static void main(String[] args) {
		// Float
		float y = 10.55f; // you must put the f at the end of any float number 
		
		// Integer (int)
		int x = 10;

		// Concatenate the y(take %f place) and x(take %d place) in a string then print this string
		System.out.printf("x = %d  and  y = %f", x, y);

	}

}

